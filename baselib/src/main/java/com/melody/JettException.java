

package com.melody;



public class JettException extends RuntimeException {

	public JettException() {
		super();
	}

	public JettException(String message, Throwable cause) {
		super(message, cause);
	}

	public JettException(String message) {
		super(message);
	}

	public JettException(Throwable cause) {
		super(cause);
	}



}
